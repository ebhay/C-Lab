// Write a c program to demostrate working of relational operator c= a>b [ 1 ]
#include<stdio.h>
int main()
{
  int a = 9;
  int b = 4;
  
  printf(" a >  b: %d \n", a > b);
  printf("a >= b: %d \n", a >= b);
  printf("a <= b: %d \n", a <= b);
  printf("a <  b: %d \n", a < b);
  printf("a == b: %d \n", a == b);
  printf("a != b: %d \n", a != b);

}